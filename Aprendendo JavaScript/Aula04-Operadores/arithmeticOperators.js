// salário
// operador de soma
const salario = 5000;
const valeAlimentacao = 500;

console.log("O salário total (incluindo VA) é: ", salario + valeAlimentacao);

// operador de subtração
const salarioBruto = 7000;
const descontos = 2000;

console.log("O salário que cai na conta é: ", salarioBruto - descontos);

// casamento
// operador de multiplicação
const valorBuffetPorPessoa = 80;
const totalDeConvidados = 100;

console.log("O valor total do buffet é: ", valorBuffetPorPessoa * totalDeConvidados);

// operador de divisão
const custoTotalCasamento = 80000;

console.log("Custo por convidado total é: ", custoTotalCasamento / totalDeConvidados);

// número pares e ímpares (módulo)

let numero = 10;
console.log("O resto da divisão de ", numero + " por 2 é: ", numero % 2);
console.log("O número " + numero + " é PAR");

numero = 11;
console.log("O resto da divisão de ", numero + " por 2 é: ", numero % 2);
console.log("O número agora é ÍMPAR");
